fn uninstall_package(package_name: &str) -> Result<(), String> {
    let install_root = PathBuf::from(OPT_PKG_ROOT).join(package_name);
    match fs::symlink_metadata(&install_root) {
        Ok(_) => {}
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => {
            return Err(format!("package {package_name} is not installed"));
        }
        Err(err) => {
            return Err(format!("failed to stat {}: {err}", install_root.display()));
        }
    }

    remove_package_stubs_from_bin(
        Path::new(OPT_PKG_ROOT),
        package_name,
        Path::new(USR_LOCAL_BIN),
    )?;
    remove_path(&install_root)?;
    refresh_post_uninstall_stubs(Path::new(OPT_PKG_ROOT), Path::new(USR_LOCAL_BIN))
}

fn remove_path(path: &Path) -> Result<(), String> {
    let metadata = fs::symlink_metadata(path)
        .map_err(|err| format!("failed to stat {}: {err}", path.display()))?;
    if metadata.file_type().is_symlink() || metadata.is_file() {
        fs::remove_file(path).map_err(|err| format!("failed to remove {}: {err}", path.display()))
    } else {
        fs::remove_dir_all(path)
            .map_err(|err| format!("failed to remove {}: {err}", path.display()))
    }
}

fn remove_package_stubs_from_bin(
    opt_root: &Path,
    package_name: &str,
    bin_dir: &Path,
) -> Result<(), String> {
    let install_root = opt_root.join(package_name);
    let manifest = load_stub_manifest(&install_root.join(STUB_MANIFEST))?;
    let shared_stubs = collect_shared_stubs(opt_root, package_name)?;

    for stub in manifest.stubs {
        if shared_stubs.contains(&stub) {
            continue;
        }
        let path = bin_dir.join(&stub);
        if path.exists() || fs::symlink_metadata(&path).is_ok() {
            remove_path(&path)?;
        }
    }

    Ok(())
}

fn collect_shared_stubs(
    opt_root: &Path,
    excluded_package: &str,
) -> Result<HashSet<String>, String> {
    let mut stubs = HashSet::new();
    let entries = match fs::read_dir(opt_root) {
        Ok(entries) => entries,
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => return Ok(stubs),
        Err(err) => return Err(format!("failed to read {}: {err}", opt_root.display())),
    };

    for entry in entries {
        let entry = entry.map_err(|err| format!("failed to read {}: {err}", opt_root.display()))?;
        let name = entry
            .file_name()
            .into_string()
            .map_err(|_| format!("non-utf8 directory name under {}", opt_root.display()))?;
        if name == excluded_package || name.starts_with('.') {
            continue;
        }

        for stub in load_stub_manifest(&entry.path().join(STUB_MANIFEST))?.stubs {
            stubs.insert(stub);
        }
    }

    Ok(stubs)
}

fn refresh_post_uninstall_stubs(opt_root: &Path, bin_dir: &Path) -> Result<(), String> {
    match fs::symlink_metadata(bin_dir) {
        Ok(_) => {}
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => return Ok(()),
        Err(err) => return Err(format!("failed to stat {}: {err}", bin_dir.display())),
    }

    for (formula, prefix) in find_supported_post_install_prefixes(opt_root)? {
        post_install_hooks::run(&formula, &prefix, bin_dir)?;
    }

    Ok(())
}

fn find_supported_post_install_prefixes(opt_root: &Path) -> Result<Vec<(String, PathBuf)>, String> {
    let mut prefixes = Vec::new();
    let entries = match fs::read_dir(opt_root) {
        Ok(entries) => entries,
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => return Ok(prefixes),
        Err(err) => return Err(format!("failed to read {}: {err}", opt_root.display())),
    };

    for entry in entries {
        let entry = entry.map_err(|err| format!("failed to read {}: {err}", opt_root.display()))?;
        let path = entry.path();
        if !path.is_dir() {
            continue;
        }

        for formula in installed_formula_receipts(&path)? {
            if post_install_hooks::supports(&formula) {
                prefixes.push((formula, path.clone()));
            }
        }
    }

    Ok(prefixes)
}

fn installed_formula_receipts(install_root: &Path) -> Result<Vec<String>, String> {
    let receipts_dir = install_root.join(RECEIPTS_DIR);
    let entries = match fs::read_dir(&receipts_dir) {
        Ok(entries) => entries,
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => return Ok(Vec::new()),
        Err(err) => return Err(format!("failed to read {}: {err}", receipts_dir.display())),
    };

    let mut formulas = Vec::new();
    for entry in entries {
        let entry =
            entry.map_err(|err| format!("failed to read {}: {err}", receipts_dir.display()))?;
        let path = entry.path();
        if path.extension().and_then(OsStr::to_str) != Some("json") {
            continue;
        }
        let Some(formula) = path.file_stem().and_then(OsStr::to_str) else {
            return Err(format!(
                "non-utf8 receipt filename under {}",
                receipts_dir.display()
            ));
        };
        formulas.push(formula.to_string());
    }
    formulas.sort();
    Ok(formulas)
}

fn sync_stubs(plan: &InstallPlan, graph: &[FormulaSpec]) -> Result<(), String> {
    if plan.mode != Mode::I {
        return Ok(());
    }

    fs::create_dir_all(USR_LOCAL_BIN)
        .map_err(|err| format!("failed to create {}: {err}", USR_LOCAL_BIN))?;
    let current = if plan.mode == Mode::I {
        let manifest = load_root_executable_manifest(&plan.root_executables_manifest_path())?;
        if manifest.stubs.is_empty() {
            collect_root_executables(&plan.install_root)?
        } else {
            let names = manifest
                .stubs
                .iter()
                .map(|value| value.as_str())
                .collect::<Vec<_>>();
            collect_declared_root_executables(&plan.install_root, &names)?
        }
    } else {
        collect_root_executables(&plan.stable_root)?
    };
    let previous = load_stub_manifest(&plan.package_manifest_path())?;

    for stub in previous.stubs {
        if !current.iter().any(|(name, _)| name == &stub) {
            let path = PathBuf::from(USR_LOCAL_BIN).join(&stub);
            if path.exists() || fs::symlink_metadata(&path).is_ok() {
                remove_path(&path)?;
            }
        }
    }

    let env_entries = build_exec_path_entries(plan, graph);
    for (name, actual_path) in &current {
        let stub_path = PathBuf::from(USR_LOCAL_BIN).join(name);
        write_stub(plan, &stub_path, actual_path, &env_entries)?;
    }

    write_stub_manifest(
        &plan.package_manifest_path(),
        &StubManifest {
            stubs: current.into_iter().map(|(name, _)| name).collect(),
        },
    )
}

fn sync_vendor_stubs(
    plan: &InstallPlan,
    graph: &[FormulaSpec],
    package: &vendor::VendorPackage,
) -> Result<(), String> {
    if plan.mode != Mode::I {
        return Ok(());
    }

    fs::create_dir_all(USR_LOCAL_BIN)
        .map_err(|err| format!("failed to create {}: {err}", USR_LOCAL_BIN))?;
    let current = collect_declared_root_executables(&plan.install_root, package.executables)?;
    let previous = load_stub_manifest(&plan.package_manifest_path())?;

    for stub in previous.stubs {
        if !current.iter().any(|(name, _)| name == &stub) {
            let path = PathBuf::from(USR_LOCAL_BIN).join(&stub);
            if path.exists() || fs::symlink_metadata(&path).is_ok() {
                remove_path(&path)?;
            }
        }
    }

    let env_entries = build_exec_path_entries(plan, graph);
    for (name, actual_path) in &current {
        let stub_path = PathBuf::from(USR_LOCAL_BIN).join(name);
        write_stub(plan, &stub_path, actual_path, &env_entries)?;
    }

    write_stub_manifest(
        &plan.package_manifest_path(),
        &StubManifest {
            stubs: current.into_iter().map(|(name, _)| name).collect(),
        },
    )
}

fn run_formula_post_install(plan: &InstallPlan, bin_dir: &Path) -> Result<(), String> {
    let outcome = post_install_hooks::run(&plan.root_formula, &plan.install_root, bin_dir)?;
    if outcome.managed_stubs.is_empty() {
        return Ok(());
    }

    let mut manifest = load_stub_manifest(&plan.package_manifest_path())?;
    for stub in outcome.managed_stubs {
        push_unique_string(&mut manifest.stubs, stub);
    }
    manifest.stubs.sort();
    write_stub_manifest(&plan.package_manifest_path(), &manifest)
}

fn collect_root_executables(root: &Path) -> Result<Vec<(String, PathBuf)>, String> {
    let mut execs = Vec::new();
    for dir in [root.join("bin"), root.join("sbin")] {
        let entries = match fs::read_dir(&dir) {
            Ok(entries) => entries,
            Err(err) if err.kind() == std::io::ErrorKind::NotFound => continue,
            Err(err) => return Err(format!("failed to read {}: {err}", dir.display())),
        };
        for entry in entries {
            let entry = entry.map_err(|err| format!("failed to read {}: {err}", dir.display()))?;
            let path = entry.path();
            if !is_executable(&path) {
                continue;
            }
            let name = entry
                .file_name()
                .to_str()
                .ok_or_else(|| format!("non-utf8 executable name under {}", dir.display()))?
                .to_string();
            execs.push((name, path));
        }
    }
    execs.sort_by(|left, right| left.0.cmp(&right.0));
    Ok(execs)
}

fn collect_declared_root_executables(
    root: &Path,
    executables: &[&str],
) -> Result<Vec<(String, PathBuf)>, String> {
    let mut execs = Vec::with_capacity(executables.len());
    for executable in executables {
        let path = ["bin", "sbin"]
            .into_iter()
            .map(|dir| root.join(dir).join(executable))
            .find(|candidate| is_executable(candidate))
            .ok_or_else(|| format!("expected executable {executable} under {}", root.display()))?;
        execs.push(((*executable).to_string(), path));
    }
    execs.sort_by(|left, right| left.0.cmp(&right.0));
    Ok(execs)
}

fn load_stub_manifest(path: &Path) -> Result<StubManifest, String> {
    let data = match fs::read(path) {
        Ok(data) => data,
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => {
            return Ok(StubManifest { stubs: Vec::new() });
        }
        Err(err) => return Err(format!("failed to read {}: {err}", path.display())),
    };
    serde_json::from_slice(&data)
        .map_err(|err| format!("failed to parse {}: {err}", path.display()))
}

fn installed_stub_paths(plan: &InstallPlan) -> Result<Vec<String>, String> {
    let mut paths = load_stub_manifest(&plan.package_manifest_path())?
        .stubs
        .into_iter()
        .map(|stub| {
            PathBuf::from(USR_LOCAL_BIN)
                .join(stub)
                .display()
                .to_string()
        })
        .collect::<Vec<_>>();
    paths.sort();
    Ok(paths)
}

fn write_stub_manifest(path: &Path, manifest: &StubManifest) -> Result<(), String> {
    let data = serde_json::to_vec_pretty(manifest)
        .map_err(|err| format!("failed to serialize stub manifest: {err}"))?;
    fs::write(path, data).map_err(|err| format!("failed to write {}: {err}", path.display()))
}

