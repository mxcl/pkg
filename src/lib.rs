pub fn main_entry() {
    let mut args = env::args_os();
    let program = args.next().unwrap_or_else(|| OsString::from("pkg"));
    let invocation = Invocation::from_program(&program);

    let result = match invocation.mode {
        Some(mode) => run_mode(mode, &invocation, args),
        None => dispatch_pkg(&invocation, args),
    };

    if let Err(err) = result {
        eprintln!("{}: {err}", invocation.name);
        process::exit(1);
    }
}

impl Invocation {
    fn from_program(program: &OsString) -> Self {
        let binary_name = Path::new(program)
            .file_name()
            .and_then(|value| value.to_str())
            .unwrap_or("pkg")
            .to_string();
        let mode = Mode::from_name(&binary_name);
        Self {
            binary_name: binary_name.clone(),
            name: binary_name,
            mode,
        }
    }

    fn for_subcommand(binary_name: &str, subcommand: &str, mode: Mode) -> Self {
        Self {
            binary_name: binary_name.to_string(),
            name: format!("{binary_name} {subcommand}"),
            mode: Some(mode),
        }
    }
}

impl Mode {
    fn from_name(name: &str) -> Option<Self> {
        match name {
            "x" | "use" => Some(Self::X),
            "i" | "install" => Some(Self::I),
            _ => None,
        }
    }

    fn canonical_name(self) -> &'static str {
        match self {
            Self::X => "use",
            Self::I => "install",
        }
    }
}

impl InstallPlan {
    fn for_x(root_formula: String) -> Self {
        let stable_root = PathBuf::from(TMP_X_ROOT).join(&root_formula);
        Self {
            mode: Mode::X,
            package_name: root_formula.clone(),
            root_formula,
            stable_root: stable_root.clone(),
            install_root: stable_root,
            tmp_root: PathBuf::from(TMP_X_ROOT).join(".tmp"),
        }
    }

    fn for_i(package_name: String, root_formula: String) -> Self {
        let stable_root = PathBuf::from(OPT_PKG_ROOT).join(&package_name);
        let install_root = PathBuf::from(OPT_PKG_ROOT).join(&package_name);
        Self {
            mode: Mode::I,
            package_name,
            root_formula,
            stable_root,
            install_root,
            tmp_root: PathBuf::from(OPT_PKG_ROOT).join(".tmp"),
        }
    }

    fn actual_target_dir(&self, formula: &str) -> PathBuf {
        if self.mode == Mode::I || formula == self.root_formula {
            self.install_root.clone()
        } else {
            self.install_root.join("pkgs").join(formula)
        }
    }

    fn stable_target_dir(&self, formula: &str) -> PathBuf {
        if self.mode == Mode::I || formula == self.root_formula {
            self.install_root.clone()
        } else {
            self.stable_root.join("pkgs").join(formula)
        }
    }

    fn receipt_path(&self, formula: &str) -> PathBuf {
        if self.mode == Mode::I {
            self.install_root
                .join(RECEIPTS_DIR)
                .join(format!("{formula}.json"))
        } else if formula == self.root_formula {
            self.install_root.join(ROOT_RECEIPT)
        } else {
            self.actual_target_dir(formula).join(RECEIPT)
        }
    }

    fn package_manifest_path(&self) -> PathBuf {
        self.install_root.join(STUB_MANIFEST)
    }

    fn root_receipt_path(&self) -> PathBuf {
        self.install_root.join(ROOT_RECEIPT)
    }

    fn root_executables_manifest_path(&self) -> PathBuf {
        self.install_root.join(ROOT_EXECUTABLES_MANIFEST)
    }
}

impl PackageStatus {
    fn is_outdated(&self) -> bool {
        self.installed_version != self.latest_version
    }
}

impl InstallProgress {
    fn new(label: &str) -> Self {
        if std::io::stderr().is_terminal() {
            let bar = ProgressBar::new(0);
            bar.set_prefix(label.to_string());
            bar.set_style(download_progress_style());
            bar.enable_steady_tick(Duration::from_millis(120));
            return Self {
                enabled: true,
                bar: Some(bar),
                state: Arc::new(Mutex::new(InstallProgressState {
                    phase: InstallProgressPhase::Download,
                })),
            };
        }

        Self {
            enabled: false,
            bar: None,
            state: Arc::new(Mutex::new(InstallProgressState {
                phase: InstallProgressPhase::Download,
            })),
        }
    }

    fn begin_download_phase(&self) {
        let mut state = self.state.lock().unwrap();
        state.phase = InstallProgressPhase::Download;
        drop(state);
        if let Some(bar) = &self.bar {
            bar.set_style(download_progress_style());
            bar.set_position(0);
            bar.set_length(0);
            bar.set_message(String::new());
        }
    }

    fn add_download_total(&self, total: Option<u64>) {
        if !self.enabled {
            return;
        }
        let Some(total) = total else {
            return;
        };
        if total == 0 {
            return;
        }
        if let Some(bar) = &self.bar {
            bar.inc_length(total);
        }
    }

    fn advance_download(&self, amount: u64) {
        if !self.enabled || amount == 0 {
            return;
        }
        if let Some(bar) = &self.bar {
            bar.inc(amount);
        }
    }

    fn begin_install_phase(&self) {
        let mut state = self.state.lock().unwrap();
        if state.phase == InstallProgressPhase::Install {
            return;
        }
        state.phase = InstallProgressPhase::Install;
        drop(state);
        if let Some(bar) = &self.bar {
            bar.set_style(install_progress_style());
            bar.set_message("staging files".to_string());
        }
    }

    fn log<S: AsRef<str>>(&self, message: S) {
        let message = sanitize_progress_message(message.as_ref());
        if message.is_empty() {
            return;
        }
        self.begin_install_phase();
        if let Some(bar) = &self.bar {
            bar.set_message(message);
        }
    }

    fn finish_with_paths(&self, paths: &[String]) {
        let message = format_installed_paths(paths);
        if let Some(bar) = &self.bar {
            bar.set_style(final_progress_style());
            bar.finish_with_message(message);
        } else {
            eprintln!("{message}");
        }
    }

    fn clear(&self) {
        if let Some(bar) = &self.bar {
            bar.finish_and_clear();
        }
    }
}

fn download_progress_style() -> ProgressStyle {
    ProgressStyle::with_template(
        "{spinner:.cyan} {prefix:.bold} [{bar:28.cyan/blue}] {percent:>3}% {bytes}/{total_bytes}",
    )
    .unwrap()
    .progress_chars("=> ")
}

fn install_progress_style() -> ProgressStyle {
    ProgressStyle::with_template("{spinner:.cyan} {prefix:.bold} {msg}").unwrap()
}

fn final_progress_style() -> ProgressStyle {
    ProgressStyle::with_template("{msg}").unwrap()
}

fn sanitize_progress_message(message: &str) -> String {
    message
        .split(|ch| ch == '\n' || ch == '\r')
        .map(str::trim)
        .filter(|line| !line.is_empty())
        .last()
        .map(|line| {
            line.chars()
                .filter(|ch| !ch.is_control())
                .collect::<String>()
                .split_whitespace()
                .collect::<Vec<_>>()
                .join(" ")
        })
        .unwrap_or_default()
}

fn format_installed_paths(paths: &[String]) -> String {
    if paths.is_empty() {
        "installed".to_string()
    } else {
        paths.join(" ")
    }
}

fn run_x(invocation: &Invocation, mut args: env::ArgsOs) -> Result<(), String> {
    let request = match parse_x_request(invocation, &mut args)? {
        Some(request) => request,
        None => return Ok(()),
    };

    let config = load_config()?;
    let graph = resolve_formula_specs(&[request.root_formula.clone()], &config, false)?;
    let root_formula = graph
        .last()
        .map(|spec| spec.name.clone())
        .ok_or_else(|| "no formula resolved".to_string())?;
    let plan = InstallPlan::for_x(root_formula);
    let progress = InstallProgress::new(&plan.package_name);

    let result = (|| {
        ensure_plan_parent_dirs(&plan)?;
        let downloads = download_bottles(&graph, &plan.tmp_root, Some(&progress))?;
        progress.begin_install_phase();
        let installs = inspect_keg_dirs(&graph, &downloads)?;
        let rewrite_rules = build_rewrite_rules(&plan, &installs);
        install_package(&config, &plan, &installs, &rewrite_rules, Some(&progress))?;

        find_executable_in_package(&plan, &graph, &request.tool).ok_or_else(|| {
            format!(
                "'{}' not found in package {}",
                request.tool, plan.package_name
            )
        })
    })();

    match result {
        Ok(exec_path) => {
            progress.clear();
            exec_tool(
                exec_path,
                &request.tool_args,
                &build_exec_path_entries(&plan, &graph),
            );
        }
        Err(err) => {
            progress.clear();
            Err(err)
        }
    }
}

fn run_i(invocation: &Invocation, mut args: env::ArgsOs) -> Result<(), String> {
    let request = match parse_i_request(invocation, &mut args)? {
        Some(request) => request,
        None => return Ok(()),
    };

    if !is_root() {
        return Err("must be run as root".to_string());
    }

    let config = load_config()?;
    for package in request.packages {
        run_i_package(&config, package)?;
    }
    Ok(())
}

fn run_i_package(config: &Config, requested: RequestedPackage) -> Result<(), String> {
    match requested {
        RequestedPackage::Auto(package_name) => {
            if let Some(package) = vendor::get(&package_name) {
                return run_i_vendor(config, package_name, package);
            }
            let root_formula = resolve_i_root_formula(&package_name)?;
            run_i_formula(config, package_name, root_formula)
        }
        RequestedPackage::HomebrewFormula(formula) => {
            run_i_formula(config, formula.clone(), formula)
        }
    }
}

fn run_i_formula(
    config: &Config,
    package_name: String,
    root_formula: String,
) -> Result<(), String> {
    let progress = InstallProgress::new(&package_name);
    let result = (|| {
        let graph = resolve_formula_specs(std::slice::from_ref(&root_formula), config, true)?;
        let root_formula = graph
            .last()
            .map(|spec| spec.name.clone())
            .ok_or_else(|| "no formula resolved".to_string())?;
        let tmp_root = PathBuf::from(OPT_PKG_ROOT).join(".tmp");
        fs::create_dir_all(&tmp_root)
            .map_err(|err| format!("failed to create {}: {err}", tmp_root.display()))?;
        let downloads = download_bottles(&graph, &tmp_root, Some(&progress))?;
        progress.begin_install_phase();
        let installs = inspect_keg_dirs(&graph, &downloads)?;
        let root_install = installs
            .iter()
            .find(|install| install.spec.name == root_formula)
            .ok_or_else(|| format!("root formula {root_formula} not present in install graph"))?;
        let plan = InstallPlan::for_i(package_name.clone(), root_formula);

        ensure_plan_parent_dirs(&plan)?;
        let rewrite_rules = build_rewrite_rules(&plan, &installs);
        install_package(config, &plan, &installs, &rewrite_rules, Some(&progress))?;
        activate_install(&plan)?;
        write_package_receipt(
            &plan.root_receipt_path(),
            &PackageReceipt {
                package_name: package_name.clone(),
                version: root_install.keg_dir_name.clone(),
                source: PackageReceiptSource::Formula {
                    root_formula: plan.root_formula.clone(),
                },
            },
        )?;
        sync_stubs(&plan, &graph)?;
        run_formula_post_install(&plan, Path::new(USR_LOCAL_BIN))?;
        installed_stub_paths(&plan)
    })();

    match result {
        Ok(paths) => {
            progress.finish_with_paths(&paths);
            Ok(())
        }
        Err(err) => {
            progress.clear();
            Err(err)
        }
    }
}

fn run_mode(mode: Mode, invocation: &Invocation, args: env::ArgsOs) -> Result<(), String> {
    match mode {
        Mode::X => run_x(invocation, args),
        Mode::I => run_i(invocation, args),
    }
}

fn run_uninstall(invocation: &Invocation, mut args: env::ArgsOs) -> Result<(), String> {
    let request = match parse_uninstall_request(invocation, &mut args)? {
        Some(request) => request,
        None => return Ok(()),
    };

    if !is_root() {
        return Err("must be run as root".to_string());
    }

    for package in request.packages {
        uninstall_package(&package)?;
    }
    Ok(())
}

fn run_outdated(invocation: &Invocation, mut args: env::ArgsOs) -> Result<(), String> {
    let request = match parse_package_status_request(invocation, &mut args, print_outdated_usage)? {
        Some(request) => request,
        None => return Ok(()),
    };

    let config = load_config()?;
    for package in resolve_package_statuses(&config, &request.selection)?
        .into_iter()
        .filter(PackageStatus::is_outdated)
    {
        println!(
            "{} {} -> {}",
            package.package_name, package.installed_version, package.latest_version
        );
    }
    Ok(())
}

fn run_list(invocation: &Invocation, mut args: env::ArgsOs) -> Result<(), String> {
    let request = match parse_package_status_request(invocation, &mut args, print_list_usage)? {
        Some(request) => request,
        None => return Ok(()),
    };

    let config = load_config()?;
    for package in resolve_package_statuses(&config, &request.selection)? {
        println!("{} {}", package.package_name, package.installed_version);
    }
    Ok(())
}

fn dispatch_pkg(invocation: &Invocation, mut args: env::ArgsOs) -> Result<(), String> {
    let Some(first_arg) = args.next() else {
        print_pkg_usage(&invocation.name);
        return Err("missing subcommand".to_string());
    };

    if is_help_flag(&first_arg) {
        print_pkg_usage(&invocation.name);
        return Ok(());
    }

    if is_version_flag(&first_arg) {
        println!("{} {}", invocation.name, env!("CARGO_PKG_VERSION"));
        return Ok(());
    }

    if matches!(first_arg.to_str(), Some("help")) {
        if let Some(topic) = args.next() {
            match topic.to_str() {
                Some(subcommand) if is_uninstall_subcommand(subcommand) => {
                    print_uninstall_usage(&format!("{} {}", invocation.binary_name, subcommand));
                }
                Some(subcommand) if is_outdated_subcommand(subcommand) => {
                    print_outdated_usage(&format!("{} {}", invocation.binary_name, subcommand));
                }
                Some(subcommand) if is_list_subcommand(subcommand) => {
                    print_list_usage(&format!("{} {}", invocation.binary_name, subcommand));
                }
                Some(subcommand) => match Mode::from_name(subcommand) {
                    Some(mode) => {
                        let nested = Invocation::for_subcommand(
                            &invocation.binary_name,
                            mode.canonical_name(),
                            mode,
                        );
                        print_mode_usage(mode, &nested.name);
                    }
                    None => print_pkg_usage(&invocation.name),
                },
                None => print_pkg_usage(&invocation.name),
            }
        } else {
            print_pkg_usage(&invocation.name);
        }
        return Ok(());
    }

    let subcommand = first_arg
        .to_str()
        .ok_or_else(|| "subcommand must be valid UTF-8".to_string())?;
    if is_uninstall_subcommand(subcommand) {
        return run_uninstall(
            &Invocation {
                binary_name: invocation.binary_name.clone(),
                name: format!("{} {subcommand}", invocation.binary_name),
                mode: None,
            },
            args,
        );
    }
    if is_outdated_subcommand(subcommand) {
        return run_outdated(
            &Invocation {
                binary_name: invocation.binary_name.clone(),
                name: format!("{} {subcommand}", invocation.binary_name),
                mode: None,
            },
            args,
        );
    }
    if is_list_subcommand(subcommand) {
        return run_list(
            &Invocation {
                binary_name: invocation.binary_name.clone(),
                name: format!("{} {subcommand}", invocation.binary_name),
                mode: None,
            },
            args,
        );
    }
    let Some(mode) = Mode::from_name(subcommand) else {
        print_pkg_usage(&invocation.name);
        return Err(format!("unknown subcommand '{subcommand}'"));
    };
    let nested = Invocation::for_subcommand(&invocation.binary_name, subcommand, mode);
    run_mode(mode, &nested, args)
}

fn run_i_vendor(
    config: &Config,
    package_name: String,
    package: vendor::VendorPackage,
) -> Result<(), String> {
    let progress = InstallProgress::new(&package_name);
    let result = (|| {
        let version = (package.version)()?;
        let vendor_install = VendorInstall { package, version };
        let dependency_formulas = vendor_install
            .package
            .dependencies
            .iter()
            .map(|value| (*value).to_string())
            .collect::<Vec<_>>();
        let graph = resolve_formula_specs(&dependency_formulas, config, false)?;
        let tmp_root = plan_tmp_root(&package_name);
        fs::create_dir_all(&tmp_root)
            .map_err(|err| format!("failed to create {}: {err}", tmp_root.display()))?;
        let dependency_state =
            resolve_dependency_install_state(&graph, &tmp_root, Some(&progress))?;
        let plan = InstallPlan::for_i(
            package_name.clone(),
            vendor_install.package.name.to_string(),
        );

        ensure_plan_parent_dirs(&plan)?;

        let dependency_current =
            dependencies_are_current(&plan, &dependency_state.installs, config)?;
        let mut dependencies_reinstalled = false;
        if !dependency_current {
            progress.begin_install_phase();
            install_dependency_formulas(
                config,
                &plan,
                &dependency_state.installs,
                Some(&progress),
            )?;
            dependencies_reinstalled = true;
        }

        if !vendor_root_is_current(
            &plan,
            &vendor_install,
            &dependency_state.installs,
            &config.bottle_tag,
        )? {
            if !dependencies_reinstalled {
                prepare_vendor_root_area(&plan)?;
            }
            install_vendor_root(&plan, &graph, &vendor_install, Some(&progress))?;
        }

        activate_install(&plan)?;
        write_package_receipt(
            &plan.root_receipt_path(),
            &PackageReceipt {
                package_name: package_name.clone(),
                version: vendor_install.version.to_string(),
                source: PackageReceiptSource::Vendor {
                    vendor_name: vendor_install.package.name.to_string(),
                },
            },
        )?;
        sync_vendor_stubs(&plan, &graph, &vendor_install.package)?;
        installed_stub_paths(&plan)
    })();

    match result {
        Ok(paths) => {
            progress.finish_with_paths(&paths);
            Ok(())
        }
        Err(err) => {
            progress.clear();
            Err(err)
        }
    }
}

fn plan_tmp_root(package_name: &str) -> PathBuf {
    let _ = package_name;
    PathBuf::from(OPT_PKG_ROOT).join(".tmp")
}

fn parse_x_request(
    invocation: &Invocation,
    args: &mut env::ArgsOs,
) -> Result<Option<XRequest>, String> {
    let Some(mut first_arg) = args.next() else {
        print_x_usage(&invocation.name);
        return Err("missing executable name".to_string());
    };

    let mut shebang_mode = false;
    if is_shebang_flag(&first_arg) {
        shebang_mode = true;
        first_arg = args.next().ok_or_else(|| {
            print_x_usage(&invocation.name);
            "missing executable name".to_string()
        })?;
    }

    if is_help_flag(&first_arg) {
        print_x_usage(&invocation.name);
        return Ok(None);
    }

    if is_version_flag(&first_arg) {
        println!("{} {}", invocation.name, env!("CARGO_PKG_VERSION"));
        return Ok(None);
    }

    let mut formulas = Vec::new();
    while let Some(formula) = parse_formula_spec(&first_arg)? {
        push_unique_string(&mut formulas, formula);
        first_arg = args.next().ok_or_else(|| {
            print_x_usage(&invocation.name);
            "missing executable name".to_string()
        })?;
    }

    if formulas.len() > 1 {
        return Err("supports a single root package".to_string());
    }

    let mut tool = first_arg
        .to_str()
        .ok_or_else(|| "tool name must be valid UTF-8".to_string())?
        .to_string();
    if tool.contains('/') {
        return Err("tool name must not contain path separators".to_string());
    }

    if shebang_mode {
        let _ = args.next();
    }
    let tool_args: Vec<OsString> = args.collect();

    let root_formula = if let Some(formula) = formulas.pop() {
        formula
    } else if let Some((base_tool, formula)) = split_versioned_tool_token(&tool) {
        tool = base_tool;
        formula
    } else {
        let db = load_db()?;
        ensure_db_schema(&db)?;
        db.entries
            .get(&tool)
            .cloned()
            .ok_or_else(|| format!("no Homebrew formula found for '{tool}'"))?
    };

    Ok(Some(XRequest {
        tool,
        tool_args,
        root_formula,
    }))
}

