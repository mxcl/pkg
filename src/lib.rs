            install_dependency_formulas(
                config,
                &staged_plan,
                &dependency_state.installs,
                Some(&progress),
            )?;
            install_vendor_dependencies(
                &staged_plan,
                &dependencies.formula_graph,
                &dependencies.vendor_installs,
                Some(&progress),
            )?;
