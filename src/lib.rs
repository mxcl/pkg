fn parse_package_name(value: &OsString) -> Result<RequestedPackage, String> {
    let package = value
        .to_str()
        .ok_or_else(|| "package name must be valid UTF-8".to_string())?
        .to_string();
    if let Some(formula) = package.strip_prefix("brew.sh/") {
        if formula.is_empty() {
            return Err("package qualifier 'brew.sh/' is missing a formula name".to_string());
        }
        if formula.contains('/') {
            return Err(
                "qualified package name must not contain additional path separators".to_string(),
            );
        }
        return Ok(RequestedPackage::HomebrewFormula(formula.to_string()));
    }
    if package.contains('/') {
        return Err("package name must not contain path separators".to_string());
    }
    Ok(RequestedPackage::Auto(package))
}

fn parse_uninstall_package_name(value: &OsString) -> Result<String, String> {
    let package = value
        .to_str()
        .ok_or_else(|| "package name must be valid UTF-8".to_string())?;
    if let Some(formula) = package.strip_prefix("brew.sh/") {
        if formula.is_empty() {
            return Err("package qualifier 'brew.sh/' is missing a formula name".to_string());
        }
        if formula.contains('/') {
            return Err(
                "qualified package name must not contain additional path separators".to_string(),
            );
        }
        return Ok(formula.to_string());
    }
    if package.contains('/') {
        return Err("package name must not contain path separators".to_string());
    }
    Ok(package.to_string())
}

