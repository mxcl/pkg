    #[test]
    fn run_formula_post_install_creates_python_dispatchers_and_tracks_them() {
        let temp = TempDir::new().unwrap();
        let opt_root = temp.path().join("opt");
        let bin_dir = temp.path().join("usr-local-bin");
        let plan = InstallPlan {
            mode: Mode::I,
            package_name: "python@3.12".to_string(),
            root_formula: "python@3.12".to_string(),
            stable_root: opt_root.join("python@3.12"),
            install_root: opt_root.join("python@3.12"),
            tmp_root: temp.path().join("tmp"),
        };
        fs::create_dir_all(&plan.install_root).unwrap();
        fs::create_dir_all(opt_root.join("python@3.13")).unwrap();
        fs::create_dir_all(&bin_dir).unwrap();
        write_stub_manifest(
            &plan.package_manifest_path(),
            &StubManifest {
                stubs: vec!["pip3.12".to_string(), "python3.12".to_string()],
            },
        )
        .unwrap();
        for name in ["python3.12", "pip3.12", "python3.13", "pip3.13"] {
            let path = bin_dir.join(name);
            fs::write(&path, b"#!/bin/sh\n").unwrap();
            let mut permissions = fs::metadata(&path).unwrap().permissions();
            permissions.set_mode(0o755);
            fs::set_permissions(&path, permissions).unwrap();
        }

        run_formula_post_install(&plan, &bin_dir).unwrap();

        let python = fs::read_to_string(bin_dir.join("python")).unwrap();
        assert!(python.contains(&bin_dir.join("python3.13").display().to_string()));
        assert!(python.contains(&bin_dir.join("python3.12").display().to_string()));
        assert!(python.find("python3.13").unwrap() < python.find("python3.12").unwrap());

        let pip = fs::read_to_string(bin_dir.join("pip")).unwrap();
        assert!(pip.contains(&bin_dir.join("pip3.13").display().to_string()));
        assert!(pip.contains(&bin_dir.join("pip3.12").display().to_string()));

        assert_eq!(
            fs::read_link(bin_dir.join("python3")).unwrap(),
            bin_dir.join("python3.13")
        );
        assert_eq!(
            fs::read_link(bin_dir.join("pip3")).unwrap(),
            bin_dir.join("pip3.13")
        );

        let manifest = load_stub_manifest(&plan.package_manifest_path()).unwrap();
        assert_eq!(
            manifest.stubs,
            vec![
                "pip".to_string(),
                "pip3".to_string(),
                "pip3.12".to_string(),
                "python".to_string(),
                "python3".to_string(),
                "python3.12".to_string(),
            ]
        );
    }

