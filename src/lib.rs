fn resolve_package_statuses(
    config: &Config,
    selection: &PackageSelection,
) -> Result<Vec<PackageStatus>, String> {
    match selection {
        PackageSelection::AllInstalled => resolve_scanned_package_statuses(
            installed_package_names(Path::new(OPT_PKG_ROOT))?,
            |package_name| resolve_package_status(config, package_name),
            |message| eprintln!("{message}"),
        ),
        PackageSelection::Requested(packages) => {
            let mut package_names = packages
                .iter()
                .map(requested_package_name)
                .collect::<Vec<_>>();
            package_names.sort();
            package_names.dedup();

            let mut statuses = Vec::with_capacity(package_names.len());
            for package_name in package_names {
                statuses.push(resolve_package_status(config, &package_name)?);
            }
            Ok(statuses)
        }
    }
}

fn resolve_scanned_package_statuses<Resolve, Warn>(
    mut package_names: Vec<String>,
    mut resolve: Resolve,
    mut warn: Warn,
) -> Result<Vec<PackageStatus>, String>
where
    Resolve: FnMut(&str) -> Result<PackageStatus, String>,
    Warn: FnMut(String),
{
    package_names.sort();
    package_names.dedup();

    let mut statuses = Vec::with_capacity(package_names.len());
    for package_name in package_names {
        if ignored_opt_directory(&package_name) {
            continue;
        }
        match resolve(&package_name) {
            Ok(status) => statuses.push(status),
            Err(err) => warn(format!(
                "warning: skipping {OPT_PKG_ROOT}/{package_name}: {err}"
            )),
        }
    }
    Ok(statuses)
}

fn ignored_opt_directory(package_name: &str) -> bool {
    package_name == "homebrew"
}

fn resolve_package_status(config: &Config, package_name: &str) -> Result<PackageStatus, String> {
    let install_root = PathBuf::from(OPT_PKG_ROOT).join(package_name);
    let metadata = fs::symlink_metadata(&install_root).map_err(|err| match err.kind() {
        std::io::ErrorKind::NotFound => format!("package {package_name} is not installed"),
        _ => format!("failed to stat {}: {err}", install_root.display()),
    })?;
    if !metadata.is_dir() {
        return Err(format!(
            "installed package root {} is not a directory",
            install_root.display()
        ));
    }

    let receipt = load_or_resolve_package_receipt(package_name, &install_root)?;
    let latest_version = match &receipt.source {
        PackageReceiptSource::Formula { root_formula } => {
            resolve_formula_latest_version(config, root_formula)?
        }
        PackageReceiptSource::Vendor { vendor_name } => resolve_vendor_latest_version(vendor_name)?,
    };

    Ok(PackageStatus {
        package_name: receipt.package_name,
        source: receipt.source,
        installed_version: receipt.version,
        latest_version,
    })
}

fn requested_package_name(package: &RequestedPackage) -> String {
    match package {
        RequestedPackage::Auto(package_name) | RequestedPackage::HomebrewFormula(package_name) => {
            package_name.clone()
        }
    }
}

fn installed_package_names(opt_root: &Path) -> Result<Vec<String>, String> {
    let entries = match fs::read_dir(opt_root) {
        Ok(entries) => entries,
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => return Ok(Vec::new()),
        Err(err) => return Err(format!("failed to read {}: {err}", opt_root.display())),
    };

    let mut packages = Vec::new();
    for entry in entries {
        let entry = entry.map_err(|err| format!("failed to read {}: {err}", opt_root.display()))?;
        let path = entry.path();
        let name = entry
            .file_name()
            .into_string()
            .map_err(|_| format!("non-utf8 directory name under {}", opt_root.display()))?;
        if name.starts_with('.') || !path.is_dir() {
            continue;
        }
        packages.push(name);
    }
    packages.sort();
    Ok(packages)
}

fn load_or_resolve_package_receipt(
    package_name: &str,
    install_root: &Path,
) -> Result<PackageReceipt, String> {
    if let Some(receipt) = load_package_receipt(&install_root.join(ROOT_RECEIPT))? {
        return Ok(receipt);
    }

    if let Some(receipt) = resolve_legacy_formula_package_receipt(package_name, install_root)? {
        return Ok(receipt);
    }

    if vendor::get(package_name).is_some() {
        return resolve_legacy_vendor_package_receipt(package_name, install_root);
    }

    Err(format!(
        "package {package_name} is installed but missing package metadata"
    ))
}

fn load_package_receipt(path: &Path) -> Result<Option<PackageReceipt>, String> {
    let data = match fs::read(path) {
        Ok(data) => data,
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => return Ok(None),
        Err(err) => return Err(format!("failed to read {}: {err}", path.display())),
    };
    let receipt = serde_json::from_slice(&data)
        .map_err(|err| format!("failed to parse {}: {err}", path.display()))?;
    Ok(Some(receipt))
}

fn write_package_receipt(path: &Path, receipt: &PackageReceipt) -> Result<(), String> {
    let data = serde_json::to_vec_pretty(receipt)
        .map_err(|err| format!("failed to serialize package receipt: {err}"))?;
    fs::write(path, data).map_err(|err| format!("failed to write {}: {err}", path.display()))
}

fn resolve_legacy_formula_package_receipt(
    package_name: &str,
    install_root: &Path,
) -> Result<Option<PackageReceipt>, String> {
    let mut candidates = vec![package_name.to_string()];
    if let Ok(root_formula) = resolve_i_root_formula(package_name) {
        if root_formula != package_name {
            candidates.push(root_formula);
        }
    }

    for root_formula in candidates {
        if let Some(receipt) =
            load_formula_package_receipt(install_root, package_name, &root_formula)?
        {
            return Ok(Some(receipt));
        }
    }

    Ok(None)
}

fn load_formula_package_receipt(
    install_root: &Path,
    package_name: &str,
    root_formula: &str,
) -> Result<Option<PackageReceipt>, String> {
    let path = install_root
        .join(RECEIPTS_DIR)
        .join(format!("{root_formula}.json"));
    let data = match fs::read(&path) {
        Ok(data) => data,
        Err(err) if err.kind() == std::io::ErrorKind::NotFound => return Ok(None),
        Err(err) => return Err(format!("failed to read {}: {err}", path.display())),
    };
    let receipt: InstallReceipt = serde_json::from_slice(&data)
        .map_err(|err| format!("failed to parse {}: {err}", path.display()))?;
    Ok(Some(PackageReceipt {
        package_name: package_name.to_string(),
        version: receipt.version,
        source: PackageReceiptSource::Formula {
            root_formula: root_formula.to_string(),
        },
    }))
}

fn resolve_legacy_vendor_package_receipt(
    package_name: &str,
    install_root: &Path,
) -> Result<PackageReceipt, String> {
    let package = vendor::get(package_name).ok_or_else(|| {
        format!("package {package_name} is installed but is not a known vendor package")
    })?;
    let version = probe_installed_vendor_version(install_root, &package)?;
    Ok(PackageReceipt {
        package_name: package_name.to_string(),
        version,
        source: PackageReceiptSource::Vendor {
            vendor_name: package.name.to_string(),
        },
    })
}

fn resolve_formula_latest_version(config: &Config, formula: &str) -> Result<String, String> {
    let info = fetch_formula_info(formula)?;
    ensure_formula_has_bottle(formula, &info, &config.bottle_tag)?;
    Ok(formula_version_string(&info))
}

fn resolve_vendor_latest_version(package_name: &str) -> Result<String, String> {
    let package = vendor::get(package_name)
        .ok_or_else(|| format!("vendor package {package_name} is not registered"))?;
    (package.version)().map(|version| version.to_string())
}

fn probe_installed_vendor_version(
    install_root: &Path,
    package: &vendor::VendorPackage,
) -> Result<String, String> {
    for executable in package.executables {
        let executable_path = ["bin", "sbin"]
            .into_iter()
            .map(|dir| install_root.join(dir).join(executable))
            .find(|candidate| is_executable(candidate));
        let Some(executable_path) = executable_path else {
            continue;
        };

        for arg in ["--version", "version", "-V", "-v"] {
            let output = match Command::new(&executable_path).arg(arg).output() {
                Ok(output) => output,
                Err(_) => continue,
            };
            if !output.status.success() {
                continue;
            }

            let mut text = String::from_utf8_lossy(&output.stdout).into_owned();
            if !output.stderr.is_empty() {
                if !text.is_empty() {
                    text.push('\n');
                }
                text.push_str(&String::from_utf8_lossy(&output.stderr));
            }
            if let Some(version) = extract_semver_from_text(&text) {
                return Ok(version.to_string());
            }
        }
    }

    Err(format!(
        "package {} is installed but missing version metadata; reinstall it",
        package.name
    ))
}

fn extract_semver_from_text(text: &str) -> Option<semver::Version> {
    for token in text.split_whitespace() {
        let token = token.trim_matches(|ch: char| {
            !ch.is_ascii_alphanumeric() && !matches!(ch, '.' | '-' | '+' | '_')
        });
        let token = token.strip_prefix('v').unwrap_or(token);
        if token.is_empty() {
            continue;
        }
        if let Ok(version) = semver::Version::parse(token) {
            return Some(version);
        }
    }
    None
}

fn ensure_plan_parent_dirs(plan: &InstallPlan) -> Result<(), String> {
    let stable_parent = plan
        .stable_root
        .parent()
        .ok_or_else(|| format!("invalid stable root {}", plan.stable_root.display()))?;
    let install_parent = plan
        .install_root
        .parent()
        .ok_or_else(|| format!("invalid install root {}", plan.install_root.display()))?;
    fs::create_dir_all(stable_parent)
        .map_err(|err| format!("failed to create {}: {err}", stable_parent.display()))?;
    fs::create_dir_all(install_parent)
        .map_err(|err| format!("failed to create {}: {err}", install_parent.display()))?;
    fs::create_dir_all(&plan.tmp_root)
        .map_err(|err| format!("failed to create {}: {err}", plan.tmp_root.display()))?;
    Ok(())
}

fn install_package(
    config: &Config,
    plan: &InstallPlan,
    installs: &[InstalledFormula],
    rewrite_rules: &[RewriteRule],
    progress: Option<&InstallProgress>,
) -> Result<(), String> {
    if package_is_current(plan, installs, &config.bottle_tag)? {
        return Ok(());
    }

    prepare_clean_install_root(plan)?;
    let results: Vec<Result<(), String>> = installs
        .par_iter()
        .map(|install| install_formula(config, plan, install, rewrite_rules, progress))
        .collect();
    for result in results {
        result?;
    }
    Ok(())
}

fn install_dependency_formulas(
    config: &Config,
    plan: &InstallPlan,
    installs: &[InstalledFormula],
    progress: Option<&InstallProgress>,
) -> Result<(), String> {
    if installs.is_empty() {
        prepare_vendor_root_area(plan)?;
        return Ok(());
    }

    let rewrite_rules = build_rewrite_rules(plan, &installs);
    install_package(config, plan, &installs, &rewrite_rules, progress)
}

fn dependencies_are_current(
    plan: &InstallPlan,
    installs: &[InstalledFormula],
    config: &Config,
) -> Result<bool, String> {
    if installs.is_empty() {
        return Ok(plan.install_root.is_dir());
    }
    package_is_current(plan, &installs, &config.bottle_tag)
}

fn vendor_root_is_current(
    plan: &InstallPlan,
    install: &VendorInstall,
    installs: &[InstalledFormula],
    bottle_tag: &str,
) -> Result<bool, String> {
    if !plan.install_root.is_dir() {
        return Ok(false);
    }
    if !installs.is_empty() {
        if !package_is_current(plan, &installs, bottle_tag)? {
            return Ok(false);
        }
    }
    for executable in install.package.executables {
        let found = ["bin", "sbin"]
            .into_iter()
            .map(|dir| plan.install_root.join(dir).join(executable))
            .any(|candidate| is_executable(&candidate));
        if !found {
            return Ok(false);
        }
    }
    Ok(true)
}

fn resolve_dependency_install_state(
    graph: &[FormulaSpec],
    tmp_root: &Path,
    progress: Option<&InstallProgress>,
) -> Result<DependencyInstallState, String> {
    if graph.is_empty() {
        return Ok(DependencyInstallState {
            _downloads: HashMap::new(),
            installs: Vec::new(),
        });
    }

    let downloads = download_bottles(graph, tmp_root, progress)?;
    let installs = inspect_keg_dirs(graph, &downloads)?;
    Ok(DependencyInstallState {
        _downloads: downloads,
        installs,
    })
}

