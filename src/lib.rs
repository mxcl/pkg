pub fn main_entry() {
    let mut args = env::args_os();
    let program = args.next().unwrap_or_else(|| OsString::from("ss"));
    let invocation = Invocation::from_program(&program);

    let result = match invocation.mode {
        Some(mode) => run_mode(mode, &invocation, args),
        None => dispatch_pkg(&invocation, args),
    };

    if let Err(err) = result {
        eprintln!("{}: {err}", invocation.name);
        process::exit(1);
    }
}

impl Invocation {
    fn from_program(program: &OsString) -> Self {
        let binary_name = Path::new(program)
            .file_name()
            .and_then(|value| value.to_str())
            .unwrap_or("ss")
            .to_string();
        let mode = Mode::from_name(&binary_name);
        Self {
            binary_name: binary_name.clone(),
            name: binary_name,
            mode,
        }
    }

    fn for_subcommand(binary_name: &str, subcommand: &str, mode: Mode) -> Self {
        Self {
            binary_name: binary_name.to_string(),
            name: format!("{binary_name} {subcommand}"),
            mode: Some(mode),
        }
    }
}

impl Mode {
    fn from_name(name: &str) -> Option<Self> {
        match name {
            "x" | "run" | "use" => Some(Self::X),
            "i" | "install" => Some(Self::I),
            _ => None,
        }
    }

    fn canonical_name(self) -> &'static str {
        match self {
            Self::X => "run",
            Self::I => "install",
        }
    }
}

